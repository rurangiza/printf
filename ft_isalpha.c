/*
 * Checks whether a character is an alphabet or not
 *
 * Returns : (1) = yes, (0) = no
*/

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || ((c >= 97 && c <= 122)))
		return (1);
	return (0);
}
